# Рассмотрим следующую задачу. Пусть у нас есть какой-то числовой вектор, и 
#нам надо в каких-то случаях вывести максимальное число в этом векторе, 
#а в каких-то случаях минимальное. 
#Мы можем сделать это с помощью дополнительной переменной

max_or_min <- function(x, type = 'max'){
  if(type == 'max'){
    k <- max(x)
  } else {
    k <- min(x)
  }
  return(k)
}

max_or_min(c(1,10,2,4,100))
max_or_min(c(1,10,2,4,100), type = 'min')

